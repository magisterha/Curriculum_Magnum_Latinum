// FunciÃ³n para cambiar el idioma
function setLanguage(lang) {
    // Verificar si el idioma existe, si no, usar espaÃ±ol por defecto
    const selectedLang = translations[lang] ? translations[lang] : translations['es'];

    // Buscar todos los elementos con el atributo data-i18n
    const elements = document.querySelectorAll('[data-i18n]');

    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (selectedLang[key]) {
            element.textContent = selectedLang[key];
        }
    });

    // Guardar preferencia y actualizar etiqueta html
    document.documentElement.lang = lang;
}

